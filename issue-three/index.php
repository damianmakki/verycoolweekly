		<h2 class="page-title">Issue Three</h2>

		<div class="campaign-section">
			<p>Lots of motion in this Issue - both some awesome showcases and some resources for learning it. Interviews with amazing designers, some beautiful things for your home (wallpaper included), and a great look at how you may be making mistakes onboarding your users.</p>
		</div>

		<div class="campaign-section">

			<div class="item">
				<a href="https://medium.com/ux-in-motion/creating-usability-with-motion-the-ux-in-motion-manifesto-a87a4584ddc"><img src="images/uxinmotion.gif" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Creating Usability with Motion: The UX in Motion Manifesto</span>
				<span class="item--creator"><a href="https://medium.com/ux-in-motion/creating-usability-with-motion-the-ux-in-motion-manifesto-a87a4584ddc/">via Medium</a></span>
				<p>A little over a year ago (!) I was turned onto the UX In Motion training videos to help build my After Effects skills and build some really cool animations to help prototype. Issara (the creator of UX In Motion) was able to convey a TON of information in an easily digestible way - just through the introductory videos. He's back with a stunning, in-depth article discussing his 12 principles of motion (accompanied with some very cool GIFs).</p> 
			</div>


			<div class="item">
				<a href="https://www.behance.net/gallery/32094133/TQ-shop-website-concept"><img src="images/tqshop.gif" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">TQ ANimation Gallery</span>
				<span class="item--creator"><a href="https://www.behance.net/gallery/32094133/TQ-shop-website-concept">via Behance</a></span>
				<p>Speaking of motion, Anton Skvortsov put together an absolutely breathtaking concept site. Bonus points if you can pick out some of Issara's 12 principles of motion!</p> 
			</div>

			<div class="item">
				<a href="https://interfacelovers.com/"><img src="images/interfacelovers.jpg" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Interviews with Amazing Designers</span>
				<span class="item--creator"><a href="https://interfacelovers.com/">via Interface Lovers</a></span>
				<p>I stumbled upon Interface Lovers thanks to Product Hunt this week. The site is home to some pretty insightful interviews with top-tier designers. Lots of great content to read through - they even integrate the designer's Spotify playlists!</p>
			</div>


			<div class="item">
				<a href="http://design-milk.com/nendo-reveals-new-adjustable-lighting-for-flos/"><img src="images/lighting.jpg" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Nendo Reveals New, Adjustable Lighting for FLOS</span>
				<span class="item--creator"><a href="http://design-milk.com/nendo-reveals-new-adjustable-lighting-for-flos/">via Design Milk</a></span>
				<p>Everyone wants beautiful things in their home. Gaku is a modular box outfitted with a lamp that's designed to be customizable. The set has myriad accessories including bowls, vases, a tray, mirrors, and bookends to fit your tastes. And that matte black? Perfect.</p>
			</div>

			<div class="item">
				<a href="https://medium.com/samsung-internet-dev/samsung-dex-brings-a-new-dimension-to-the-mobile-web-f80d7edcab29"><img src="images/dex.jpeg" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">A phone or a computer or both?</span>
				<span class="item--creator"><a href="https://medium.com/samsung-internet-dev/samsung-dex-brings-a-new-dimension-to-the-mobile-web-f80d7edcab29">via Medium</a></span>
				<p>While there have been attempts to bridge the gap between phone and desktop before, but most have ultimately failed due to unimaginative UX experiences, lack of processing power, or just a lack of use. Samsung's popular S8 may prove to have the popularity to make DeX, Samsung's take on a desktop experience running from a mobile device a hit. Plus who doesn't love seeing Progressive Web Apps get some attention?!</p>
			</div>


			<div class="item">
				<a href="https://dribbble.com/shots/3277984-Corner"><img src="images/corner.gif" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Corner</span>
				<span class="item--creator"><a href="https://dribbble.com/shots/3277984-Corner">via Dribbble</a></span>
				<p>Just a beautiful display of motion design from Kate Laguta for M2H agency.</p>
			</div>

			<div class="item" style="width: 100%;">
				<a href="https://www.fastcodesign.com/90124014/this-wallpaper-doubles-as-a-power-source"><img src="images/light.gif" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Electric Wallpaper</span>
				<span class="item--creator"><a href="https://www.fastcodesign.com/90124014/this-wallpaper-doubles-as-a-power-source">via Fast Company</a></span>
				<p>While essentially an art installation, Flavor Paper - a wallpaper company from Brooklyn - put together Conduct which is on display at NYCxDesign. It's an exploration of how practical conductive ink could be used as both a functional and artistic piece of design.</p>
			</div>

			<div class="item">
				<a href="https://ux.useronboard.com/bulletproof-user-onboarding-f34ee03f655f"><img src="images/bulletproof.png" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Bulletproof User Onboarding</span>
				<span class="item--creator"><a href="https://ux.useronboard.com/bulletproof-user-onboarding-f34ee03f655f">via User Onboard</a></span>
				<p>I may be saving this primarily for myself this week, but Samuel's (awesome) article discusses how user onboarding is such a fragile (yet completely essential) component of online products. We've all experienced those helpful tooltips when we first use a new online service. How helpful are they really, though?</p>
			</div>

		</div>

