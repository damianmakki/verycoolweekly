		<h2 class="page-title">Issue Five</h2>

		<div class="campaign-section">
			<p>Welcome to Issue Five of Very Cool Weekly.<br><br>As a designer, I often forget my knowledge isn't always shared with the people I'm designing for. It's dangerous because I run the risk of falling into a trap of designing for myself and neglecting the needs of the end user.<br><br>This concept is addressed at least twice this week - specifically from Jon's "Who Are We Designing For?" and Trigger's interesting concept for encouraging creative brainstorming.<br><br>Are you enjoying Very Cool Weekly? Share it with a friend!<br><br>Until next week. Have a great weekend!</p>
		</div>

		<div class="campaign-section">

			<div class="item">
				<a href="https://medium.com/ux-power-tools/who-are-we-designing-for-designing-for-buy-in-part-1-of-3-7fc8d4320e33"><img src="images/designingforbuyin.jpg" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Who Are We Designing For?</span>
				<span class="item--creator"><a href="https://medium.com/ux-power-tools/who-are-we-designing-for-designing-for-buy-in-part-1-of-3-7fc8d4320e33/">via Medium</a></span>
				<p>I loved this article because I could relate to it so much. At my previous position, I often found myself designing for a specific INTERNAL audience to "sell" my design to. This was easy because it was a smaller company and I knew who I'd be presenting to. A design isn't just pretty typography, or something that tells a story and can be sold, or a business - it's all those things. Jon did a great job surveying the high-level elements here, though I'd stop and ask if this is HOW we should be designing. At the end of the day, designing for users is paramount, not just buy-in from internal decision-makers.</p> 
			</div>


			<div class="item">
				<a href="https://officehours.io/streams/article/3fmrbdnQymhdrp2zZ"><img src="images/slowdown.jpg" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Slow the Fuck Down</span>
				<span class="item--creator"><a href="https://officehours.io/streams/article/3fmrbdnQymhdrp2zZ">via OfficeHours.io</a></span>
				<p>I'm getting better at not over-optimizing my life and trying to lifehack everything. I've come to realize I don't ALWAYS need to be reading something, I don't ALWAYS need to be maximizing my productivity. I don't ALWAYS need to be learning. They're, of course, things I love to do, but they lose benefit when they start stressing me out. Eric's essay outlines the need to press pause and start setting priorities so you can live your life - not the other way around. It can be brutal at times, but Eric has some great insights.</p> 
			</div>

			<div class="item">
				<a href="https://www.theguardian.com/world/2017/may/08/pepe-the-frog-creator-kills-off-internet-meme-co-opted-by-white-supremacists"><img src="images/pepe.jpg" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Pepe is Dead</span>
				<span class="item--creator"><a href="https://www.theguardian.com/world/2017/may/08/pepe-the-frog-creator-kills-off-internet-meme-co-opted-by-white-supremacists">via The Outline</a></span>
				<p>Pepe the frog is dead - killed off by his creator, Matt Furie. For the uninitiated, Pepe the frog started gaining traction as an alt-right symbol a few months ago and took on a meaning not intended by Matt. While amusing, the official death of the cartoon character is a great example of how design can influence culture. It's also troubling that a design can take on a life of it's own not always intended by it's creator.</p>
			</div>


			<div class="item">
				<a href="http://www.trytriggers.com/"><img src="images/triggers.jpg" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">A Brilliantly Creative way of Brainstorming</span>
				<span class="item--creator"><a href="http://www.trytriggers.com/">via Triggers</a></span>
				<p>One of the biggest issues I face as a designer is combatting my tendency to fall into tried-and-true design patterns. I often find myself implementing solutions to problems that I've often solved before. Triggers, a set of physical cards that come in a few different packs (the Essential Deck, User-Centric Deck, Innovation Deck and the Serendipity Deck), and provide designers and developers a way of thinking outside the box. One of my favorites? "What if the idea was about procrastination?" from the Serendipity deck.</p>
			</div>

			<div class="item">
				<a href="https://dribbble.com/shots/3489745-Compact-responsive-datepicker"><img src="images/datepicker.gif" style="width: 100%; " /></a>
			</div>
			<div class="item">
				<span class="item--title">Compact Responsive Datepicker</span>
				<span class="item--creator"><a href="https://dribbble.com/shots/3489745-Compact-responsive-datepicker">via Dribbble</a></span>
				<p>This week I found myself looking at design patterns for a iOS date picker. Personally, I think the stock implementation is pretty terrible. Misha's innovative date picker, on the other hand, beautifully combines a ton of usability with some major flexibility.</p>
